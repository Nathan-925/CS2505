/*
 * bitParity - returns 1 if x contains an odd number of 0's
 *   Examples: bitParity(5) = 0, bitParity(7) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 4
 */
int bitParity(int x) {
   /*By using xor on two halves of the number until it is only 1 bit, the final bit will be one if there is an
   odd number of 1s and 0s. This is because xor will result in a 1 when used on different bits an odd number of times.*/
   x ^= x>>16;
   x ^= x>>8;
   x ^= x>>4;
   x ^= x>>2;
   x ^= x>>1;
   return x&1;
}
