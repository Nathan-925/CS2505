/* 
 * byteSwap - swaps the nth byte and the mth byte
 *  Examples: byteSwap(0x12345678, 1, 3) = 0x56341278
 *            byteSwap(0xDEADBEEF, 0, 2) = 0xDEEFBEAD
 *  You may assume that 0 <= n <= 3, 0 <= m <= 3
 *  Legal ops: ! ~ & ^ | + << >>
 *  Max ops: 25
 *  Rating: 2
 */
int byteSwap(int x, int n, int m) {
   /*The bytes are removed from x by right shifting by n or m bytes then using a 0xFF mask to isolate them. They are then
   left shifted by the other argument to place them in the correct spot. A mask for the origional number that is all 1s other
   than where the bytes are to be inserted is created by left shifting 0xFF by n and m bytes, then bit flipping it. The mask is
   applied to x to clear the spots for the bytes to be inserted.*/
   int byte1 = ((x>>(n<<3))&0xFF) << (m<<3);
   int byte2 = ((x>>(m<<3))&0xFF) << (n<<3);

   int mask = ~((0xFF<<(n<<3)) | (0xFF<<(m<<3)));

   int result = (x&mask)|byte1|byte2;
   return result;
}
