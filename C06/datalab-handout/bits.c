/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
   /*If both x and y have the opposite sign as the result of addition, then the addition has overflowed.
   Xor sets the sign bit to 1 if the numbers are different signs, then and is used to check if that is true
   for both x and y. The sign bit is isolated then fipped to create the answer because it will be 1 if overflow occured.*/
   int result = x+y;
   return !((((x^result)&(y^result))>>31)&1);
}
