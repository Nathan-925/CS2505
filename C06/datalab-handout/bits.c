/* 
 * bitMatch - Create mask indicating which bits in x match those in y
 *            using only ~ and & 
 *   Example: bitMatch(0x7, 0xE) = 0x6
 *   Legal ops: ~ & |
 *   Max ops: 14
 *   Rating: 1
 */
int bitMatch(int x, int y) {
   /*The matching bits are found by using and on x and y, which finds where both numbers have a 1, as well as the
   bit flipped verwsions of x and y, finding matching 0s. The two results are combined using or to create the result.*/
   return (x&y) | (~x&~y);
}
